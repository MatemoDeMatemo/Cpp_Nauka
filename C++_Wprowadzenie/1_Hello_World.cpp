#include <iostream>   // Makro - definiowane na poczatku kodu. Mozna deklarowac tutaj paczki kodu, funkcje, wartosci oraz wiecej.

// Sercem kodu w C++ jest zawartosc funkcji main(). To co sie w niej znajdzie zostanie wykonane. 
int main() {
   // cout to odpowiednik ~print(). znajduje sie w przestrzeni - "paczce" std. 
   // << – operator przesyłania danych do strumienia
   std::cout << "Hello, World!" << std::endl; 
   
   return 0; // Kod konczymy return 0. Jesli funkcja zwroci nam 0, bedziemy wiedzieli ze wykonala sie poprawnie w calosci.
}
// By tworzyc komentarze stosujemy: // lub dla segmentow:
